package Part1;

public class Increment {
	public static void main(String args[]) {
		int c;
		
		
		c = 5;
		System.out.println(c);
		System.out.println(c++); //prints 5 then increments
		System.out.println(c);
		
		
		System.out.println();
		
		c = 5;
		System.out.println(c);
		System.out.println(++c); //prints 6 then stays there
		System.out.println(c);
		
		
	}

}
